            <h1>My Approach</h1>
            
            <span>
                Over the years, I have picked up much that can only come with years of toiling in a specific field. I have paid for all
                that I have learnt with extensive patience, time, and effort. The principles that I mastered during this process are not
                apparent to many, even during the most exhaustive technical interviews. Therefore, I offer potential employers a sneak
                peek into some of the guidelines I developed for a clearer understanding of the way I operate.<br /><br />
                
                <ul>
                    
                    <li>
                        <a href="#approach-1" class="js-toggle">I do not take projects under unrealistic conditions</a>
                        <div id="approach-1">
                            I always make it a point to conduct a comprehensive analysis of project requirements, budget, and specific
                            conditions which the client emphasizes upon, and then proceed to the planning stage. More often than not,
                            I find a way to exceed customer expectations; but sometimes, the best I can do is say "mission impossible"
                            when I know the available resources just won’t be enough to meet the exacting conditions which the client
                            has stated. I know what it takes to create high-quality software, and make it a point to never underestimate
                            resources.
                        </div>
                    </li>

                    <li>
                        <a href="#approach-2" class="js-toggle">I always plan my work in advance</a>
                        <div id="approach-2">
                            I now know for a fact that thorough planning pays out every time. For each project, I carefully develop a
                            game plan that fits the project requirements, after weighing each and every option at considerable length.
                            Based on each customer’s individual preferences, parameters such as budget, team members, available
                            technologies, are manipulated to deliver best results. Furthermore, I always make sure to resolve any
                            uncertainties beforehand, enabling me to create the final product that perfectly fits requirements and
                            expectations. This reduces the risk of any issues in the future. I have also developed the habit of asking
                            insightful questions to attain clarity when it comes to possible variants and the respective pros and cons.
                        </div>
                    </li>

                    <li>
                        <a href="#approach-3" class="js-toggle">I sort out reasonable costs from irrelevant ones </a>
                        <div id="approach-3">
                            When it comes to software development, one wrong decision can lead to drastic increases in costs, resulting
                            in loss of precious resources that could otherwise have been put to productive use. I make it a point to
                            manage resources effectively and consider the monetary effects of decisions while on any project for this
                            very reason. In addition, there are often situations when a client can save on costs by bypassing a
                            particular problem, instead of trying to solve. In such a scenario, I consider it my responsibility to
                            inform the client of effects on budget and possible alternative solutions that they could implement to save
                            on expenses.
                        </div>
                    </li>

                    <li>
                        <a href="#approach-4" class="js-toggle">I prefer to avoid redundant dependencies</a>
                        <div id="approach-4">
                            Every project has at least some dependencies such as specific code libraries and plugins, and at times,
                            using these makes good sense. On the other hand, sometimes redundant or unreasonable inclusions are done
                            which provide no benefit in functionality, and can potentially introduce bugs in the application.
                            Introducing dependencies almost always results in a trade-off between control and time/efforts/resources.
                            To avoid redundant dependencies, I can write in-house code if need be. Any functionality based on the core
                            technologies on my profile can be built from scratch if such is the requirement.
                        </div>
                    </li>

                    <li>
                        <a href="#approach-5" class="js-toggle">I know how to use abstractions and interfaces</a>
                        <div id="approach-5">
                            One of the best inventions in programming is the capability to split future projects into modules
                            separated with an API; this enables the developer to focus on working on the API to optimize the
                            application and writing code without the need of considerable planning. The benefits of this approach are
                            numerous; as it lets multiple teams develop several modules simultaneously without conflicting with each
                            other. Functional testing for any system module, enforcing TDD, or even replacing/rewriting system modules
                            without affecting entire system can be done using this approach. Managing these abstractions can get
                            complicated at times though, with a developer discovering at a later stage that they are in over their
                            heads. I have been working on abstractions for a number of years, and have optimized my approach to always
                            find the right balance.
                        </div>
                    </li>

                    <li>
                        <a href="#approach-6" class="js-toggle">I know how to manage resources</a>
                        <div id="approach-6">
                            When working on any project, I always make sure to resolve any issues that might cause impediments in the
                            operations before I start. Once I know clearly what needs to be done, I can start immediately working on
                            simultaneous processes to stay on top of deadlines. I know the entire process of software development and
                            therefore find it easy to plan my work in advance. This saves time, effort, and respects budget constraints.
                        </div>
                    </li>

                    <li>
                        <a href="#approach-7" class="js-toggle">Code clarity, reusability and maintainability is critical to me</a>
                        <div id="approach-7">
                            It’s impossible to have repeat clients if they experience problems maintaining what you have created. On
                            the other hand, writing documentation takes significant timing. I always discuss this aspect with my
                            customers and explain the benefits of having accompanying documentation. In case of no documentation, the
                            customer may find it quite difficult to get the application maintained by someone else at a later stage.
                        </div>
                    </li>

                    <li>
                        <a href="#approach-8" class="js-toggle">I pay special attention to small details</a>
                        <div id="approach-8">
                            I believe, this is what makes the differences between professionals and amateurs, most of all. During my
                            work, I carefully eliminate every inconsistency that comes my way, and replace it, anticipating user
                            expectations. I’m driven by the principle of the science stating that anything can be off the mark, or be
                            in excess, with the right point lies somewhere in between. I think this applies to everything, including
                            programming, web design, and life itself. 
                        </div>
                    </li>

